#------------------------------------------------
# 원소/요소가 존재하는 지 검사
# - 존재하는 원소/요소 인경우  : '빙고'
# - 존재하지 않는 원소/요소 인경우 : '존재하지 않음'
#------------------------------------------------
data=[3,6,9,12,15]

num=11
# num  이 data의 원소 인지 아닌지 즉. 멤버여부 검사


if num in data :
    print('빙고')
else :
    print('존재하지 않음')


    
