#------------------------------------------------
# 도시 명을 입력 받아서 6대 광역시에 해당 하는 지 결과를 출력
# - 존재하는 경우  : '광역도시군요'
# - 존재하지 않는 경우  : '일반도시 군요'
#------------------------------------------------
data =['부산' ,'대구', '인천' ,'광주','대전' ,'울산']



d= input('도시명을 입력해주세요 :').strip()

if d in data :
    print(f'{d}는 광역시이군요.')
else :
    print(f'{d}는 일반도시 입니다.')



